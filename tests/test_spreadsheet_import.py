def test_imports_csv_as_single_sheet() -> None:
    csv_file = _csv_bytes(_valid_frame("APP-950", "Billing Core", "Finance"))

    assert list_excel_sheets(csv_file, filename="drift.csv") == ["CSV Upload"]
    preview = preview_excel_workbook(csv_file, filename="drift.csv")
    result = import_excel_workbook(csv_file, filename="drift.csv")

    assert preview[0]["import_role"] == "Standalone drift rows"
    assert result.row_count == 1
    assert result.sheets == ["CSV Upload"]
    assert result.rows[0]["app_id"] == "APP-950"


def test_uploaded_rows_refresh_database_and_feed_analytics(demo_db_path: Path) -> None:
    workbook = _workbook_bytes(
        {
            "Wave1": _valid_frame("APP-910", "Executive Ledger", "Finance"),
            "Wave2": _valid_frame("APP-911", "Claims Bridge", "Claims"),
        }
    )
    result = import_excel_workbook(workbook, selected_sheets=["Wave1", "Wave2"])

    replaced = replace_applications_rows(demo_db_path, result.rows)
    response = run_query_plan(DriftDatabase(demo_db_path), ai_risk_intelligence())

    assert replaced == 2
    assert response.error is None
    assert {row["app_id"] for row in response.rows} == {"APP-910", "APP-911"}
