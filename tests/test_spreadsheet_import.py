def _workbook_bytes(sheets: dict[str, pd.DataFrame]) -> bytes:
    buffer = BytesIO()
    with pd.ExcelWriter(buffer, engine="openpyxl") as writer:
        for sheet_name, frame in sheets.items():
            frame.to_excel(writer, index=False, sheet_name=sheet_name)
    return buffer.getvalue()


def _csv_bytes(frame: pd.DataFrame) -> bytes:
    return frame.to_csv(index=False).encode("utf-8")


def _valid_frame(app_id: str, app_name: str, product: str) -> pd.DataFrame:
    return pd.DataFrame(
        [
            {
                "Application ID": app_id,
                "Application Name": app_name,
                "Product": product,
                "Data Center": "DAL-1",
                "Approved Version": "10.2",
                "Detected Version": "10.0",
                "RTO Score": 2,
                "Status": "Open",
                "Exemption Requested": "No",
                "In Scope": "Yes",
                "Drift Detected On": "2026-01-15",
            }
        ]
    )


def test_imports_multiple_sheets_and_normalizes_aliases() -> None:
    workbook = _workbook_bytes(
        {
            "Payments": _valid_frame("APP-900", "Treasury Gateway", "Payments"),
            "Risk": _valid_frame("APP-901", "Risk Console", "Risk"),
        }
    )

    assert list_excel_sheets(workbook) == ["Payments", "Risk"]
    result = import_excel_workbook(workbook, selected_sheets=["Payments", "Risk"], filename="drift.xlsx")

    assert result.row_count == 2
    assert result.sheets == ["Payments", "Risk"]
    assert result.rows[0]["app_id"] == "APP-900"
    assert result.rows[0]["business_owner"] == "Unassigned"
    assert result.rows[0]["exemption_requested"] == "N"
    assert result.rows[0]["in_scope"] == "Y"


def test_spreadsheet_import_reports_missing_required_columns() -> None:
    workbook = _workbook_bytes({"BadSheet": pd.DataFrame([{"Application ID": "APP-1"}])})

    with pytest.raises(SpreadsheetImportError) as exc_info:
        import_excel_workbook(workbook, selected_sheets=["BadSheet"])

    assert "Missing essential columns" in str(exc_info.value)
    assert exc_info.value.issues[0].sheet == "BadSheet"


def test_import_skips_non_data_sheets_when_valid_rows_exist() -> None:
    workbook = _workbook_bytes(
        {
            "Cover": pd.DataFrame([{"Notes": "Export generated by CMDB"}]),
            "Apps": _valid_frame("APP-902", "Claims Hub", "Claims"),
        }
    )

    result = import_excel_workbook(workbook, selected_sheets=["Cover", "Apps"])

    assert result.row_count == 1
    assert result.sheets == ["Apps"]
    assert result.rows[0]["app_id"] == "APP-902"
    assert result.warnings
    assert result.warnings[0].sheet == "Cover"


